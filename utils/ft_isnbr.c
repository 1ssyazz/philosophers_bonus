#include "../philo.h"

int	ft_isnbr(char *nbr)
{
	if (!nbr)
		return (0);
	if (*nbr == '+' || *nbr == '-')
		nbr++;
	while (*nbr)
	{
		if (*nbr < '0' || *nbr > '9')
			return (0);
		if (*nbr >= '0' && *nbr <= '9')
			nbr++;
	}
	return ((*nbr == 0));
}
